#include <iostream>

using namespace std;

//X대학 M교수님은 프로그래밍 수업을 맡고 있다.교실엔 학생이 30명이 있는데, 학생 명부엔 각 학생별로 1번부터 30번까지 출석번호가 붙어 있다.
//교수님이 내준 특별과제를 28명이 제출했는데, 그 중에서 제출 안 한 학생 2명의 출석번호를 구하는 프로그램을 작성하시오.
// 입력을 출석번호로 받는데 출석번호가 없는 애들만 모아서 그중 작은 번호를 그 다음 작은 번호를 두번째로 출력
int main()
{
	ios::sync_with_stdio(false); // C++ 입출력 속도 높임
	cin.tie(NULL);               // 입력과 출력 분리하여 속도 높임
	
	bool n[30] = { false }; // 출석 체크용
	int num;

	// 제출한 학생 번호 입력 받기
	for (int i = 0; i < 28; i++)
	{
		cin >> num;
		n[num - 1] = true; // 출석 번호는 1부터 시작해서 -1
	}

	// 미제출 학생 번호 출력
	for (int i = 0; i < 30; i++)
	{
		if (n[i] == false)
		{
			cout << i + 1 << endl;
		}
	}

	return 0;
}