#include <iostream>
#include <string>
using namespace std;

// 입력 받은 대로 출력하는 프로그램 작성
// 입력이 주어진다. 입력은 최대 100줄로 이루어져있고, 알파벳 소문자, 대문자, 공백, 숫자로만 이루어져 있다.
// 각 줄은 100글자를 넘지 않으며, 빈 줄은 주어지지 않는다.
// 또, 각 줄은 공백으로 시작하지 않고, 공백으로 끝나지 않는다.
int main()
{
	string arr;

	while (true)
	{
		if (getline(cin, arr)) // 공백까지 한줄로 입력 받음
		{
			cout << arr << endl;
		}
		else
		{
			break;
		}
	}

	return 0;
}